/**
 * Configures how the text component will render contents.
 */
export interface ReactionTestOptions {
    /**
     * Hides the primary icon.
     */
    hideIcon?: boolean;

    /**
     * Hides the secondary icon.
     */
    hideSecondary?: boolean;

    /**
     * Hides the title text.
     */
    hideTitle?: boolean;
}
